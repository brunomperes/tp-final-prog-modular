    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // Emitir fatura
    	if (os.getStatus() != OrdemDeServico.STATUS.CONCLUIDA.ordinal()){
    		JOptionPane.showMessageDialog(null, "Acao nao permitida, status invalido");
    		return;
    	}
    	os.setStatus(OrdemDeServico.STATUS.EM_COBRANCA.ordinal());
    	try {
			Main.ordemdeservicoDao.update(os);
			List<ItemOrcamento> listaItems = Main.itemOrcamentoDao.findAllItemsFromOS(os);
			double valorImposto = OrdemDeServico.getValorImpostoNoOrcamento(listaItems);
			double total = OrdemDeServico.getValorTotalOrcamento(listaItems);
			JOptionPane.showMessageDialog(null, "Ordem de servico alterada para EM COBRANCA.\nValor total do or√ßamento: R$" + total + "\nDos quais o total de ISS e: R$" + valorImposto);
			disableButtons();
		} catch (SQLException e) {
			e.printStackTrace();
		}
    }//GEN-LAST:event_jButton6ActionPerformed

